
x = 10
print("Initial value of x:", x)
# Using += operator to add 5 to x
x += 5
print("Value of x after using += operator:", x)
# Using -= operator to subtract 3 from x
x -= 3
print("Value of x after using -= operator:", x)
#  Using *= operator to multiply x by 2
x *= 2
print("Value of x after using *= operator:", x)
#  Using /= operator to divide x by 2
x /= 2
print("Value of  x after using /= operator:", x)



